void main() {
  // Example of declaring different data types in Dart

  // Integer (whole number)
  final int x = 10;

  // Double (decimal number)
  final double y = 10.5;

  // String (text)
  String str = 'Raphael';

  // Boolean (true/false)
  bool f = false;

  // --- Printing values ---
  print('x = $x');             // Prints: x = 10
  print('y = $y');             // Prints: y = 10.5
  print('x + 5 = ${x + 5}');   // Prints result of an expression (15)
  print('str = $str');         // Prints the string variable
  print('f = $f');             // Prints: f = false
}
